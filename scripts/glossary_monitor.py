GITHUB_API = "https://api.github.com"
GITHUB_ORG = "Labs64"
REPO_NAME_RE = re.compile(r"^labs64\.io")

DOC_EXTENSIONS = {".md", ".yaml", ".yml"}
MAX_FILE_BYTES = 60_000
SKIP_DIRS = {".git", "node_modules", "target", "build", "dist", ".github"}

FIXED_REPOS = [
    "labs64.io-checkout",
    "labs64.io-auditflow",
    "labs64.io-payment-gateway",
    "labs64.io-docs",
]

SCRIPT_DIR    = Path(__file__).resolve().parent
MANUAL_PATH   = SCRIPT_DIR.parent / "GLOSSARY-MANUAL.md"
AUTO_PATH     = SCRIPT_DIR / ".glossary-auto.md"
GLOSSARY_PATH = SCRIPT_DIR.parent / "GLOSSARY.md"
STATE_PATH    = SCRIPT_DIR / ".glossary-state.json"

OPENROUTER_BASE_URL = "https://openrouter.ai/api/v1"
# Fallback chain passed to OpenRouter's native multi-model routing.
# OpenRouter tries each model in order automatically; we only handle retries
# for transient failures (rate limits, upstream overload) at the call level.
OPENROUTER_MODELS = [
    "nvidia/nemotron-3-ultra-550b-a55b:free",
    "openai/gpt-oss-120b:free",
    "openai/gpt-oss-20b:free",
]
_RETRY_DELAYS = [1, 2, 4, 8]  # seconds between successive retries

MAX_PROMPT_CHARS = 180_000

# Output table header (3-column, used in GLOSSARY.md)
TABLE_HEADER = "| Term | Context | Definition |\n|------|---------|------------|"

# Internal auto-file header (4-column, includes Source for link building)
AUTO_TABLE_HEADER = "| Term | Context | Definition | Source |\n|------|---------|------------|--------|"

# Fallback doc paths per context module (format: repo/path or just repo for root).
# The default branch is resolved at runtime via the GitHub API — never hardcoded.
# Only confirmed-public repos are listed; add entries as more repos go public.
CONTEXT_DOC_PATHS: dict[str, str] = {
    "Checkout":        "labs64.io-docs/checkout/checkout-service-spec-en.md",
    "Payment Gateway": "labs64.io-docs/payment-gateway/readme.md",
    "AuditFlow":       "labs64.io-auditflow",
}

# Runtime cache: repo_name → default branch (populated lazily or via prefetch in main())
_branch_cache: dict[str, str] = {}

GLOSSARY_GENERATED_COMMENT = (
    "<!-- Generated by scripts/glossary_monitor.py — do not edit directly.\n"
    "     To add or change terms manually, edit GLOSSARY-MANUAL.md instead. -->"
)


