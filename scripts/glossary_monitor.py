def get_default_branch(repo: str, token: Optional[str] = None) -> str:
    """
    Return the default branch for a repo, queried from the GitHub API.

    Results are cached in _branch_cache for the process lifetime.
    Falls back to 'master' if the repo is inaccessible (private / not found).
    Public repos can be queried without a token (subject to the unauthenticated
    rate limit of 60 req/hr); pass a token in main() to use the authenticated limit.
    """
    if repo not in _branch_cache:
        headers: dict[str, str] = {"Accept": "application/vnd.github+json",
                                    "X-GitHub-Api-Version": "2022-11-28"}
        if token:
            headers["Authorization"] = f"Bearer {token}"
        resp = requests.get(
            f"{GITHUB_API}/repos/{GITHUB_ORG}/{repo}",
            headers=headers,
            timeout=30,
        )
        _branch_cache[repo] = (
            resp.json().get("default_branch", "master") if resp.ok else "master"
        )
    return _branch_cache[repo]


def source_to_url(path: str) -> str:
    """
    Convert a source path to a full GitHub URL.

    Accepted formats:
      - 'repo-name/path/to/file.md#anchor'  →  full blob URL with anchor
      - 'repo-name/path/to/file.md'         →  full blob URL
      - 'repo-name'                          →  repo root URL
      - '' or already an https:// URL       →  returned as-is (empty → empty)

    The default branch is looked up from _branch_cache (pre-populated in main(),
    or fetched on-demand for public repos via the unauthenticated GitHub API).
    """
    if not path or path.startswith("http"):
        return path
    slash = path.find("/")
    if slash == -1:
        return f"https://github.com/Labs64/{path}"
    repo = path[:slash]
    rest = path[slash + 1:]
    branch = get_default_branch(repo)   # cache hit after main() pre-populates
    return f"https://github.com/Labs64/{repo}/blob/{branch}/{rest}"


def build_linked_context(context_str: str, source_str: str = "") -> str:
    """
    Build a linked markdown context string.

    context_str: plain comma-separated module names, e.g. "Checkout, Payment Gateway"
    source_str:  comma-separated source paths matching each module in order.
                 Empty position falls back to CONTEXT_DOC_PATHS for that module.
    """
    contexts = [c.strip() for c in context_str.split(",")]
    sources  = [s.strip() for s in source_str.split(",")] if source_str.strip() else []

    result = []
    for i, ctx in enumerate(contexts):
        raw  = sources[i] if i < len(sources) else ""
        path = raw or CONTEXT_DOC_PATHS.get(ctx, "")
        url  = source_to_url(path) if path else ""
        result.append(f"[{ctx}]({url})" if url else ctx)
    return ", ".join(result)


def extract_rows(text: str) -> list[str]:
    """Return data rows from a markdown table, skipping header and separator lines."""
    rows = []
    for line in text.splitlines():
        s = line.strip()
        if not s.startswith("|"):
            continue
        inner = s.replace("|", "").strip()
        if not inner or set(inner) <= {"-", " "}:
            continue                          # separator row
        if inner.lower().startswith("term"):
            continue                          # header row
        rows.append(s)
    return rows


def row_sort_key(row: str) -> str:
    """Alphabetical sort key: term text, stripped of bold markers, lowercased."""
    cols = row.split("|")
    term = cols[1].strip().strip("*").strip() if len(cols) > 1 else row
    return re.sub(r"\[([^\]]+)\]\([^)]*\)", r"\1", term).lower()


def _to_output_row(row: str, is_auto: bool) -> tuple[str, str]:
    """
    Parse a table row and return (sort_key, 3-column output row).

    Auto rows (4-column) carry a Source column used to build per-term links.
    Manual rows (3-column) fall back to CONTEXT_DOC_PATHS for linking.
    In both cases any pre-existing markdown links in Context are stripped
    first (backward compatibility) so re-linking always starts from plain names.
    """
    cols = row.split("|")
    # Columns: [empty, term, context, definition, (source), empty]
    if len(cols) < 5:
        return "", row
    term = cols[1].strip()
    ctx  = cols[2].strip()
    defn = cols[3].strip()
    # Source column present in 4-column (auto) rows; absent in 3-column (manual) rows
    src  = cols[4].strip() if (is_auto and len(cols) >= 6) else ""

    # Strip any existing markdown links from the context value so we can re-link cleanly
    ctx_plain = re.sub(r"\[([^\]]+)\]\([^)]*\)", r"\1", ctx)

    linked_ctx = build_linked_context(ctx_plain, src)
    sort_key   = re.sub(r"[\[\]()_*`]", "", term).lower().strip()
    return sort_key, f"| {term} | {linked_ctx} | {defn} |"


def merge_glossary(manual_text: str, auto_text: str) -> str:
    """
    Merge manual and auto table rows into one sorted 3-column table.
    When the same term appears in both, the manual entry wins.
    """
    auto_rows: dict[str, str] = {}
    for r in extract_rows(auto_text):
        k, v = _to_output_row(r, is_auto=True)
        if k:
            auto_rows[k] = v

    manual_rows: dict[str, str] = {}
    for r in extract_rows(manual_text):
        k, v = _to_output_row(r, is_auto=False)
        if k:
            manual_rows[k] = v

    merged = {**auto_rows, **manual_rows}   # manual overwrites auto on collision
    sorted_rows = sorted(merged.values(), key=row_sort_key)
    return TABLE_HEADER + "\n" + "\n".join(sorted_rows) + "\n"


def _reachable(url: str, token: Optional[str]) -> bool:
    """Return True if the resource behind *url* exists.

    GitHub blob URLs (https://github.com/Labs64/<repo>/blob/<branch>/<path>)
    are checked via the Contents API so private repos work with the token and
    anchors (#section) are stripped before the request (HTTP can't validate them).
    All other URLs fall back to a plain HEAD request.
    """
    base = url.split("#")[0]   # anchors can't be validated over HTTP
    gh = re.match(
        r"https://github\.com/Labs64/([^/]+)/blob/([^/]+)/(.+)", base
    )
    if gh:
        repo, branch, path = gh.groups()
        api_url = (
            f"{GITHUB_API}/repos/{GITHUB_ORG}/{repo}/contents/{path}"
            f"?ref={branch}"
        )
        headers: dict[str, str] = {
            "Accept": "application/vnd.github+json",
            "X-GitHub-Api-Version": "2022-11-28",
        }
        if token:
            headers["Authorization"] = f"Bearer {token}"
        try:
            resp = requests.get(api_url, headers=headers, timeout=15)
            return resp.status_code == 200
        except Exception:
            return False
    # Repo-root URLs and anything else: plain HEAD
    try:
        resp = requests.head(base, timeout=15, allow_redirects=True)
        return resp.status_code < 400
    except Exception:
        return False


def check_and_strip_links(table: str, token: Optional[str]) -> str:
    """Verify every [text](url) in *table* is reachable.

    Unreachable links are replaced with plain text so a bad LLM-generated path
    never lands in GLOSSARY.md.  Each failure is reported to stderr.
    Unique URLs are deduplicated before checking to minimise API calls.
    """
    urls = dict.fromkeys(re.findall(r"\[[^\]]+\]\(([^)]+)\)", table))
    bad: set[str] = set()
    for url in urls:
        if not _reachable(url, token):
            print(f"  WARNING: unreachable link stripped — {url}", file=sys.stderr)
            bad.add(url)

    if not bad:
        return table

    def _strip(m: re.Match) -> str:  # type: ignore[type-arg]
        return m.group(1) if m.group(2) in bad else m.group(0)

    return re.sub(r"\[([^\]]+)\]\(([^)]+)\)", _strip, table)


def write_glossary(manual_text: str, auto_text: str, token: Optional[str] = None) -> None:
    """Regenerate GLOSSARY.md from both sources."""
    table = merge_glossary(manual_text, auto_text)
    print("Checking link reachability…")
    table = check_and_strip_links(table, token)
    GLOSSARY_PATH.write_text(
        GLOSSARY_GENERATED_COMMENT + "\n\n# Glossary\n\n" + table,
        encoding="utf-8",
    )


