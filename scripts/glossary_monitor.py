def build_docs_block(docs: dict[str, str]) -> str:
    parts, total = [], 0
    for path, content in docs.items():
        block = f"=== {path} ===\n{content}\n"
        if total + len(block) > MAX_PROMPT_CHARS:
            parts.append(f"=== {path} === [omitted — character limit reached]\n")
            break
        parts.append(block)
        total += len(block)
    return "\n".join(parts)


SYSTEM_PROMPT = """\
You are a technical writer maintaining the AI-generated section of a project glossary \
for the Labs64.IO platform. A separate manually curated section exists — your job is to \
keep only the AI-generated section accurate and complete based on documentation changes.

Critical output rule: your entire response must be a single markdown table — nothing else. \
No preamble, no reasoning, no analysis, no summary, no code fences. \
Start your response with the header row (| Term | …) and end with the last data row.\
"""

USER_PROMPT_TMPL = """\
## Manually curated terms (context only — do not duplicate or modify)

These terms are already defined and will always appear in the final glossary.
Do NOT include any of them in your response, even if the changed files mention them.

{manual_section}

## Current AI-generated glossary entries

{auto_section}

## Documentation files changed since the last run

These are ONLY the files added or modified across all labs64.io-* repositories
since the glossary was last updated — not the full documentation corpus.

{changed_docs}
{removed_section}
---

Update the AI-generated entries where the changed files justify it:

1. **Add** terms from the changed files absent from both sections above.
2. **Improve** AI-generated definitions made clearer by the changed files.
3. **Expand** the Context column for AI-generated terms confirmed in additional modules.
4. **Remove** an AI-generated term only if every file mentioning it has been deleted
   (listed under "Removed files") and no other documentation uses it. When in doubt, keep it.

Constraints:
- Only include terms used in human-readable prose — not source-code symbols (field names,
  class names, method names, API path parameters).
  If a source-code identifier clearly represents a meaningful domain concept, promote it to
  the natural-language term instead of using the identifier as the glossary entry.
  Example: a class named `CorrelationIdFilter` should become "Correlation ID", not the class name.
- Skip trivial terms (common English words, generic framework names like Spring Boot or Vue).
- Return a 4-column markdown table: header row + data rows.
  Columns: **Term** | **Context** | **Definition** | **Source**
- Terms in bold, sorted alphabetically.
- Context: comma-separated module names in plain text (e.g. "Checkout, Payment Gateway").
  Do NOT add markdown links — the build script links them automatically.
- Source: for each context entry (same order), provide the relative path to the documentation
  file that most directly defines or describes the term, in the format
  "repo-name/path/to/file.md#section-anchor". Use the file paths from the === headers above.
  If a context module has no matching file in the provided docs, leave that position empty.
  Multiple sources are comma-separated (one per context entry).
- Definitions: concise prose — no bullet points, no inline code snippets.

Return ONLY the markdown table (header row + data rows). No commentary, no fences, no thinking.
Your response must start with | and every line must start with |.
If no changes are warranted, return the current AI-generated section unchanged.
"""


def _extract_table(content: str) -> Optional[str]:
    """Return pipe-only lines from *content*, or None if there are none.

    Reasoning models (e.g. Nemotron) emit chain-of-thought before the table.
    Filtering to lines that start with | strips that preamble safely.
    """
    lines = [l for l in content.splitlines() if l.startswith("|")]
    return ("\n".join(lines) + "\n") if lines else None


def call_llm(
    manual_text: str,
    auto_text: str,
    changed_docs: dict[str, str],
    removed_paths: list[str],
) -> str:
    """Call the LLM via OpenRouter and return the updated auto section table.

    Model failover is delegated to OpenRouter's native multi-model routing
    (extra_body models + route=fallback).  This function only retries on
    transient failures (rate limits, upstream overload) where waiting helps.
    """
    client = OpenAI(
        base_url=OPENROUTER_BASE_URL,
        api_key=os.environ["OPENROUTER_API_KEY"],
    )

    removed_section = ""
    if removed_paths:
        removed_section = (
            "\n## Removed files\n\n"
            + "\n".join(f"- {p}" for p in removed_paths)
            + "\n\n"
        )

    messages = [
        {"role": "system", "content": SYSTEM_PROMPT},
        {
            "role": "user",
            "content": USER_PROMPT_TMPL.format(
                manual_section=manual_text.strip(),
                auto_section=auto_text.strip(),
                changed_docs=build_docs_block(changed_docs),
                removed_section=removed_section,
            ),
        },
    ]

    last_error: str = ""

    for attempt in range(len(_RETRY_DELAYS) + 1):
        response = client.chat.completions.create(
            model=OPENROUTER_MODELS[0],          # primary; fallbacks via extra_body
            max_tokens=8192,
            messages=messages,
            extra_headers={
                "HTTP-Referer": "https://github.com/Labs64/labs64.io-docs",
                "X-Title": "Labs64 Glossary Monitor",
            },
            extra_body={
                "models": OPENROUTER_MODELS,     # OpenRouter fallback chain
                "route": "fallback",
            },
        )

        # OpenRouter surfaces errors (rate limits, upstream failures, etc.)
        # as a null choices list rather than an HTTP error code.
        if not response.choices or response.choices[0].message.content is None:
            finish = (
                response.choices[0].finish_reason if response.choices else "no choices"
            )
            last_error = f"finish_reason={finish!r}  {response.model_dump_json()}"
            if attempt < len(_RETRY_DELAYS):
                delay = _RETRY_DELAYS[attempt]
                print(
                    f"  retry {attempt + 1}: {delay} s  (error: {last_error})",
                    file=sys.stderr,
                )
                time.sleep(delay)
                continue
            break

        table = _extract_table(response.choices[0].message.content)
        if table is None:
            last_error = (
                "response contained no markdown table rows:\n"
                + response.choices[0].message.content
            )
            if attempt < len(_RETRY_DELAYS):
                delay = _RETRY_DELAYS[attempt]
                print(
                    f"  retry {attempt + 1}: {delay} s  (error: no table rows in response)",
                    file=sys.stderr,
                )
                time.sleep(delay)
                continue
            break

        return table

    raise RuntimeError(f"All retries exhausted. Last error:\n{last_error}")


# ---------------------------------------------------------------------------
# Entry point
# ---------------------------------------------------------------------------

def main() -> None:
    token = os.environ.get("GH_TOKEN") or os.environ.get("GITHUB_TOKEN")
    if not token:
        print("ERROR: set GH_TOKEN or GITHUB_TOKEN.", file=sys.stderr)
        sys.exit(1)
    if not os.environ.get("OPENROUTER_API_KEY"):
        print("ERROR: set OPENROUTER_API_KEY.", file=sys.stderr)
        sys.exit(1)
    if not MANUAL_PATH.exists():
        print(f"ERROR: {MANUAL_PATH} not found.", file=sys.stderr)
        sys.exit(1)

    state = load_state()

    # 1. Discover repos
    print("Discovering labs64.io-* repositories…")
    repos = discover_repos(token)
    print(f"  Repos: {repos}")

    # 2. Pre-populate default-branch cache (authenticated → higher rate limit).
    #    source_to_url() reads from this cache, so all URL generation after this
    #    point uses the correct branch per repo without extra API calls.
    print("Fetching default branches…")
    for repo in repos:
        branch = get_default_branch(repo, token)
        print(f"  {repo}: {branch}")

    # 3. Determine what changed across repos
    all_changed: dict[str, str] = {}
    all_removed: list[str] = []
    new_state: dict[str, str] = dict(state)

    for repo in repos:
        print(f"  Checking {repo}…", end=" ", flush=True)
        head_sha = get_head_sha(token, repo)
        if head_sha is None:
            print("inaccessible — skipped")
            continue

        stored_sha = state.get(repo)
        if stored_sha == head_sha:
            print("no new commits — skipped")
            new_state[repo] = head_sha
            continue

        if stored_sha is None:
            print("new repo, fetching all docs…", end=" ", flush=True)
            docs = fetch_all_docs(token, repo)
            all_changed.update(docs)
            print(f"{len(docs)} file(s)")
        else:
            changed_paths, removed_paths = get_changed_doc_paths(
                token, repo, stored_sha, head_sha
            )
            if changed_paths == ["__FULL_FETCH__"]:
                print("large changeset, fetching all docs…", end=" ", flush=True)
                docs = fetch_all_docs(token, repo)
                all_changed.update(docs)
                print(f"{len(docs)} file(s)")
            else:
                fetched = 0
                for fp in changed_paths:
                    content = fetch_file(token, repo, fp)
                    if content is not None:
                        all_changed[f"{repo}/{fp}"] = content
                        fetched += 1
                all_removed.extend(f"{repo}/{p}" for p in removed_paths)
                print(
                    f"{fetched} changed file(s)"
                    + (f", {len(removed_paths)} removed" if removed_paths else "")
                )

        new_state[repo] = head_sha

    # 3. Skip LLM call if no doc files changed
    if not all_changed and not all_removed:
        print("No documentation changes detected — glossary is up to date.")
        sys.exit(0)

    print(
        f"\n{len(all_changed)} changed file(s), {len(all_removed)} removed — "
        f"calling OpenRouter (models: {', '.join(OPENROUTER_MODELS)})…"
    )

    # 4. Read sources
    manual_text = MANUAL_PATH.read_text(encoding="utf-8")

    # Extract only the table portion of the auto file so that:
    #   a) the LLM sees the table it must update (not the human-readable preamble), and
    #   b) the change comparison is table-vs-table (not table vs. preamble+table).
    # Bootstrap with a minimal 4-column header matching the expected LLM contract.
    if AUTO_PATH.exists():
        auto_file_text = AUTO_PATH.read_text(encoding="utf-8")
        current_auto_table = "\n".join(
            line for line in auto_file_text.splitlines()
            if line.startswith("|")
        ) + "\n"
        if not current_auto_table.strip():
            current_auto_table = AUTO_TABLE_HEADER + "\n"
    else:
        current_auto_table = AUTO_TABLE_HEADER + "\n"

    # 5. Call LLM — manual terms are context only; LLM returns updated 4-column auto table
    proposed_auto = call_llm(manual_text, current_auto_table, all_changed, all_removed)

    # 6. Write auto source and regenerate GLOSSARY.md when the auto section changed.
    #    Context links are built from the Source column inside merge_glossary();
    #    no post-processing of the LLM output is needed here.
    glossary_changed = proposed_auto.strip() != current_auto_table.strip()
    if glossary_changed:
        AUTO_PATH.write_text(
            "# Glossary — AI-Generated Entries\n\n"
            "Internal source file maintained by `scripts/glossary_monitor.py`. "
            "Do not edit manually.\n\n"
            + proposed_auto + "\n",
            encoding="utf-8",
        )
        write_glossary(manual_text, proposed_auto, token)
        print("GLOSSARY.md regenerated.")
    else:
        print("LLM found no glossary changes needed.")

    # 8. Always persist state after an LLM call
    save_state(new_state)
    print("State file updated.")

    # 9. Signal GitHub Actions
    gha_output = os.environ.get("GITHUB_OUTPUT")
    if gha_output:
        with open(gha_output, "a", encoding="utf-8") as fh:
            fh.write(f"glossary_changed={'true' if glossary_changed else 'false'}\n")
            fh.write("state_changed=true\n")


if __name__ == "__main__":
    main()
