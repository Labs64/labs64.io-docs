#!/usr/bin/env python3
"""
Glossary Monitor
================
Scans all labs64.io-* repositories for documentation changes and uses an
LLM via OpenRouter to keep the glossary up to date.

File layout
-----------
GLOSSARY-MANUAL.md        — manually maintained terms (source of truth for curated entries)
scripts/.glossary-auto.md — AI-maintained terms (internal; updated by this script)
GLOSSARY.md               — generated output: manual + auto merged and sorted alphabetically

The LLM receives manual terms as read-only context so it never duplicates them.
On each run only the auto source is updated; GLOSSARY.md is always regenerated from both.

Token-efficiency strategy
--------------------------
scripts/.glossary-state.json tracks the last-processed commit SHA per repository.

  - Repos whose HEAD SHA matches the stored SHA are skipped entirely.
  - Changed repos use the GitHub compare API to fetch only modified doc files.
  - Repos seen for the first time receive a full fetch (one-off bootstrap cost).

The LLM is called only when at least one doc file has actually changed.
The state file is committed only after an LLM call.

Usage (local):
    export GH_TOKEN=<github-pat>
    export OPENROUTER_API_KEY=<key>
    python scripts/glossary_monitor.py
"""

