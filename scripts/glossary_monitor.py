def _gh_headers(token: str) -> dict[str, str]:
    return {
        "Authorization": f"Bearer {token}",
        "Accept": "application/vnd.github+json",
        "X-GitHub-Api-Version": "2022-11-28",
    }


def discover_repos(token: str) -> list[str]:
    found: set[str] = set(FIXED_REPOS)
    url: Optional[str] = f"{GITHUB_API}/orgs/{GITHUB_ORG}/repos"
    params: dict = {"per_page": 100, "type": "all"}
    while url:
        resp = requests.get(url, headers=_gh_headers(token), params=params, timeout=30)
        if resp.status_code == 404:
            print(f"WARNING: org '{GITHUB_ORG}' not found or token lacks org read scope.",
                  file=sys.stderr)
            break
        resp.raise_for_status()
        for repo in resp.json():
            if REPO_NAME_RE.match(repo["name"]):
                found.add(repo["name"])
        url = resp.links.get("next", {}).get("url")
        params = {}
    return sorted(found)


def get_head_sha(token: str, repo: str) -> Optional[str]:
    branch = get_default_branch(repo, token)
    url = f"{GITHUB_API}/repos/{GITHUB_ORG}/{repo}/commits/{branch}"
    resp = requests.get(url, headers=_gh_headers(token), timeout=30)
    if resp.status_code in (404, 403):
        return None
    resp.raise_for_status()
    return resp.json()["sha"]


def get_changed_doc_paths(
    token: str, repo: str, base_sha: str, head_sha: str
) -> tuple[list[str], list[str]]:
    url = f"{GITHUB_API}/repos/{GITHUB_ORG}/{repo}/compare/{base_sha}...{head_sha}"
    resp = _gh_get(token, url)
    if resp is None:
        return [], []
    data = resp.json()

    if data.get("ahead_by", 0) > 300:
        return ["__FULL_FETCH__"], []

    changed, removed = [], []
    for f in data.get("files", []):
        filename: str = f["filename"]
        if Path(filename).suffix.lower() not in DOC_EXTENSIONS:
            continue
        if any(part in SKIP_DIRS for part in Path(filename).parts):
            continue
        (removed if f["status"] == "removed" else changed).append(filename)
    return changed, removed


def fetch_file(token: str, repo: str, file_path: str) -> Optional[str]:
    url = f"{GITHUB_API}/repos/{GITHUB_ORG}/{repo}/contents/{file_path}"
    resp = _gh_get(token, url)
    if resp is None:
        return None
    data = resp.json()
    if data.get("size", 0) > MAX_FILE_BYTES:
        return None
    return base64.b64decode(data.get("content", "")).decode("utf-8", errors="ignore")


def _gh_get(token: str, url: str, retries: int = 3, backoff: float = 2.0) -> Optional[requests.Response]:
    """
    GET a GitHub API URL with retry on transient 5xx errors.
    Returns None (and logs a warning) if all retries are exhausted.
    Immediately returns None for 403/404 (permanent non-retriable).
    """
    for attempt in range(1, retries + 1):
        resp = requests.get(url, headers=_gh_headers(token), timeout=30)
        if resp.status_code in (403, 404):
            return None
        if resp.status_code < 500:
            resp.raise_for_status()
            return resp
        # 5xx: transient server error — wait and retry
        if attempt < retries:
            print(
                f"  WARNING: {resp.status_code} for {url} "
                f"(attempt {attempt}/{retries}), retrying in {backoff:.0f}s…",
                file=sys.stderr,
            )
            time.sleep(backoff)
            backoff *= 2
        else:
            print(
                f"  WARNING: {resp.status_code} for {url} after {retries} attempts — skipping.",
                file=sys.stderr,
            )
    return None


def _fetch_tree(token: str, repo: str, path: str, out: dict[str, str]) -> None:
    url = f"{GITHUB_API}/repos/{GITHUB_ORG}/{repo}/contents/{path}"
    resp = _gh_get(token, url)
    if resp is None:
        return
    items = resp.json()
    if not isinstance(items, list):
        return
    for item in items:
        name = item["name"]
        if item["type"] == "dir":
            if name not in SKIP_DIRS:
                _fetch_tree(token, repo, item["path"], out)
        elif item["type"] == "file":
            if (Path(name).suffix.lower() in DOC_EXTENSIONS
                    and item.get("size", 0) <= MAX_FILE_BYTES):
                content = fetch_file(token, repo, item["path"])
                if content is not None:
                    out[f"{repo}/{item['path']}"] = content


def fetch_all_docs(token: str, repo: str) -> dict[str, str]:
    docs: dict[str, str] = {}
    _fetch_tree(token, repo, "", docs)
    return docs


